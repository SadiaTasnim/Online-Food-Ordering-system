    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        /*SQL Server runs on top of Windows.SQL Server is actually dozens of 
      modules.This module is called when needed.The query processor translates 
      SQL into a form of code which the other server modules can use.SQL Server 
      Management Studio gives us a single location to manage our SQL Servers,
      develop and test our databases and SQL code.*/
        
        /* Here we've created a Query which will insert the name,service and 
        comment in MySQL database table named "feedback" whenever the user 
        gives a feedback*/
        String name = jTextField1.getText();
        String service = jcomboservice.getSelectedItem().toString();
        String comment = jtextcomment.getText();
       
       PreparedStatement ps;
       String query = "INSERT INTO `feedback`( `name`, `service`, `comment`) VALUES (?,?,?)";
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            ps.setString(1, name);
            ps.setString(2, service);
            ps.setString(3, comment);
            if(ps.executeUpdate()>0){
                 JOptionPane.showMessageDialog(null, "Thanks for your Feedback");
            }
           
        } catch (SQLException ex) {
            Logger.getLogger(Feedback_Page.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

