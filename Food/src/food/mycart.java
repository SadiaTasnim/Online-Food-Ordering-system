    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
        //Here we've created an arraylist to count the total amount of food ordered
        List<String> numdata = new ArrayList<String>();
        for (int count = 0; count < jTable1.getRowCount(); count++){
              numdata.add(jTable1.getValueAt(count, 3).toString());
        }
        int sum = 0;
        for(String num : numdata){
            String[] data = num.split(" ");
            int number = Integer.parseInt(data[0]);
            sum += number;
        }
        jLabel1.setText(String.valueOf(sum) + " tk");
    }//GEN-LAST:event_jLabel2MouseClicked

